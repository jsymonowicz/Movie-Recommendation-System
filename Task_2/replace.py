#provide the sentence
sentence1 = "The!quick!brown!fox!jumps!over!the!lazy!dog."

#replace "!" with " "
sentence1 = sentence1.replace("!", " ")
print(sentence1)

#capitalize each letter
sentence1 = sentence1.upper()
print(sentence1)

#reverse the capitalized sentence
sentence1 = sentence1[::-1]
print(sentence1)